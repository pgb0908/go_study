package main

import (
	"fmt"
	"time"
)

func say(msg string, count int) {
	for i := 0; i < count; i++ {
		fmt.Printf("%s (%d)\n", msg, i+1)
		time.Sleep(100 * time.Millisecond)
	}
}

func main() {
	// =========================================================
	// 고루틴 없이 순차 실행
	// =========================================================
	fmt.Println("=== 순차 실행 ===")
	start := time.Now()
	say("순차-A", 3)
	say("순차-B", 3)
	fmt.Printf("순차 실행 시간: %v\n\n", time.Since(start))

	// =========================================================
	// go 키워드로 고루틴 시작
	// 고루틴은 독립적으로 실행됩니다 (메인과 병렬)
	// =========================================================
	fmt.Println("=== 고루틴 병렬 실행 ===")
	start = time.Now()

	go say("고루틴-A", 3) // 별도 고루틴에서 실행
	go say("고루틴-B", 3) // 별도 고루틴에서 실행

	// 문제: 메인 함수가 먼저 끝나면 고루틴도 종료됩니다!
	// 임시 해결책으로 Sleep 사용 (좋지 않은 방법)
	// 올바른 방법은 WaitGroup이나 채널 사용 (04_sync_waitgroup에서 다룸)
	time.Sleep(500 * time.Millisecond)
	fmt.Printf("고루틴 실행 시간: %v\n\n", time.Since(start))

	// =========================================================
	// 익명 함수로 고루틴
	// =========================================================
	fmt.Println("=== 익명 함수 고루틴 ===")
	for i := 1; i <= 3; i++ {
		i := i // 루프 변수 캡처 버그 방지 (고루틴 클로저 주의!)
		go func() {
			fmt.Printf("고루틴 %d 실행\n", i)
		}()
	}
	time.Sleep(100 * time.Millisecond)

	// =========================================================
	// 고루틴은 매우 가볍습니다: 수천 개도 OK
	// =========================================================
	fmt.Println("\n=== 고루틴 1000개 ===")
	done := make(chan bool)
	for i := 0; i < 1000; i++ {
		go func(n int) {
			_ = n * n // 간단한 계산
			if n == 999 {
				done <- true
			}
		}(i)
	}
	<-done
	fmt.Println("1000개 고루틴 완료!")
}
