package main

import "fmt"

func main() {
	// =========================================================
	// FizzBuzz: 조건문 + 반복문 통합 연습
	// 규칙:
	//   3의 배수 → "Fizz"
	//   5의 배수 → "Buzz"
	//   3과 5 모두의 배수 → "FizzBuzz"
	//   그 외 → 숫자 그대로
	// =========================================================
	fmt.Println("=== FizzBuzz (1~30) ===")
	for i := 1; i <= 30; i++ {
		switch {
		case i%15 == 0: // 15의 배수 = 3과 5의 공배수 (순서 중요!)
			fmt.Print("FizzBuzz ")
		case i%3 == 0:
			fmt.Print("Fizz ")
		case i%5 == 0:
			fmt.Print("Buzz ")
		default:
			fmt.Printf("%d ", i)
		}
	}
	fmt.Println()

	// =========================================================
	// 별 피라미드 출력: 중첩 for 연습
	// =========================================================
	fmt.Println("\n=== 별 피라미드 ===")
	rows := 5
	for i := 1; i <= rows; i++ {
		// 공백 출력 (오른쪽 정렬 효과)
		for j := 0; j < rows-i; j++ {
			fmt.Print(" ")
		}
		// 별 출력
		for j := 0; j < 2*i-1; j++ {
			fmt.Print("*")
		}
		fmt.Println()
	}

	// =========================================================
	// 구구단 출력
	// =========================================================
	fmt.Println("\n=== 구구단 (2단~3단) ===")
	for dan := 2; dan <= 3; dan++ {
		fmt.Printf("--- %d단 ---\n", dan)
		for i := 1; i <= 9; i++ {
			fmt.Printf("%d × %d = %d\n", dan, i, dan*i)
		}
	}
}
